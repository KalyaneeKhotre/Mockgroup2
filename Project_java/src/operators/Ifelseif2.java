package operators;

public class Ifelseif2 {
	public static void main(String[] args) {
		int a=50;
		if(a==10) {
			System.out.println("a is 10");
		}
		else if(a==20) {
			System.out.println("a is 20");
		}
		else if(a==30) {
			System.out.println("a is 30");
		}
		System.out.println("a is not present");
	}

}
